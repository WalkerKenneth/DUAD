def test_empty_list():
    sorted_list = []
    bubble_sort(sorted_list)

    assert sorted_list == []


def test_non_list_parameters():
    with pytest.raises(TypeError):
        bubble_sort(None)
    with pytest.raises(TypeError):
        bubble_sort(True)
    with pytest.raises(TypeError):
        bubble_sort(245)


